        <h1 class="texto1">Jogo da velha</h1>
<br>
<br>
<p class="p1">O jogo se tornou popular na Inglaterra, durante o século XIX, quando mulheres se reuniam com amigas para conversar e fazer bordados. No entanto, assim que ficavam mais velhas, alguma delas tinham dificuldade de costurar por causa do problema nas vistas.</p>
<p class="p2">Sendo assim, passaram a utilizar botões não utilizados, ou tabuleiros improvisados, para disputar a partida de “noughts and crosses” ( ou “nós e cruzes”, em português). O nome fazia uma referência direta a termos comuns na prática do bordado.</p>
<p class="p3">Ademais, como as adeptas ao jogo eram principalmente senhoras idosas, quando chegou ao Brasil o passatempo ganhou o nome de “jogo da velha”.</p>
<p class="p4">Apesar disso, a história escavações arqueológicas indicam que já no século XIV a.C., o jogo já existia em áreas do Antigo Egito. Além disso, passatempos similares também existiam em regiões da China Antiga, América pré-colombiana e Império Romano.</p>


